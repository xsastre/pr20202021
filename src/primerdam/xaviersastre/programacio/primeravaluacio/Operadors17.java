public class Operadors17 {
    public static void main(String[] args) {
        int x=1;
        x=x+1;
        System.out.println(x);
        x=1;
        x+=1;
        System.out.println(x);
        x=1;
        ++x;
        System.out.println(x);
        x=1;
        x++;
        System.out.println(x);
    }
}
